<h2>Who invented the ground-source heat pump?</h2>

<p><img src="http://cdn4.explainthatstuff.com/ground-source-heat-pump-patent-1949.png" height="470" width="350" ALT="Early design for a ground-source heat pump taking heat from a deep water well, designed by Marvin M. Smith in 1946/1949" class="irightno"></P>

<P>
Whom do we have to thank for this brilliant idea? The theory of <a href="engines.html">heat engines</A> (machines that can
generate mechanical energy by heating fuel) largely began with French engineer Nicolas Sadi Carnot (1796&ndash;1832),
who laid the foundations for the modern science of <B>thermodynamics</B> (how heat moves).
British scientist <a href="http://www-history.mcs.st-and.ac.uk/Biographies/Thomson.html" target="_blank">Lord Kelvin</A> (William Thomson,
for whom the Kelvin <a href="thermometers.html">temperature scale</A> is named)
built on Carnot's work and figured out the theory of heat pumps (how a machine can move heat energy
from one place to another). Kelvin's work was concerned with the theoretical movement of heat; efficient, <I>practical</I> heat pumps only really
appeared in the early decades of the 20th century when electric power became widespread, giving us such indispensable inventions as the electric
refrigerator and air conditioner. Mexican-born Swiss engineer <B>Heinrich Zoelly</B> invented the modern,
electrically powered ground-source heat pump in 1912. The earliest US patent I've found is the one shown here, which was granted to Marvin Smith and Emory Kemler of Muncie Gear Works, Inc. on February 8, 1949.</P>

<h3>How it works</h3>
<P>
Smith and Kemler's 1940s water-well heat pump is <I>not</I> the world's first geothermal pump, but it's the earliest one for which I can find a decent diagram. Like most heat pumps, it can run as either a heater or a cooler. Here, I've colored and numbered to show how it works as a heat pump. Although this diagram looks confusing a a glance, it's simpler than it appears: there are two separate fluid loops connected by a pair of heat exchangers (the red and blue boxes near the top). The lower loop removes heat from the water well at the bottom, while the upper loop carries that heat to whatever the pump is heating. In a bit more detail, here's how it works: (1) Heat is extracted from a deep water well, passes up through valves (2) to a heat exchanger (3), before returning through the circuit to a cooler, higher part of the same well (4). The heat exchanger passes
the heat to a fluid in a closed loop, where it gives up its heat to the "receiver" (6,  the room or whatever else is being heated),
before passing back around the loop to pick up some more. You can read a detailed explanation in <a href="http://www.google.com/patents/US2461449" target="_blank">US Patent #2,461,449: Heat pump using deep well for a heat source</A>. Artwork by courtesy of US Patent and Trademark Office.</P>
