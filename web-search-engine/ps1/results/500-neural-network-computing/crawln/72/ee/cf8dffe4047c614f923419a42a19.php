<p>by <a href="chris-woodford.html">Chris Woodford</a>. <span class="italic">Last updated: April 26, 2015. </span></p>
<meta itemprop="alternativeHeadline" content="How heat pumps work">
<meta itemprop="description" content="How ground-source heat pumps extract heat buried underground and transport it indoors to heat your home.">
<meta itemprop="datePublished" content="2008-06-12">
<meta itemprop="dateModified" content="2015-04-26">


<p itemprop="articleBody"><span class="dropcap">N</span>o matter how well insulated your
home seems to be, heat will always leak out on a cold day, making you
a little bit colder every minute. On days like this, wouldn't it be
great if we could throw nature into reverse so our homes actually got
hotter? What if you could flick a switch and make the heat flow in
the opposite direction? It sounds absurd, but you can actually buy a
home heating system that does just that! <span class="bold">Heat pumps</span> literally suck
heat in from the ground or the air outside to warm up your home.
Even though this simple idea is at least a century old, it's still very much an unexploited technology.
Let's take a closer look at how it works!</p>

<p><span class="credit">Photo: This <a href="ecohomes.html">eco house</A> produces all its own energy from 
<a href="solarcells.html">solar panels</A>, <a href="passive-solar.html">passive solar</a> (large areas
of glass windows), and a heat pump. The house is literally sucking in heat from the ground underneath it! 
Photo courtesy of <a href="http://www.doedigitalarchive.doe.gov/" target="_blank">US Department of Energy</a>.
</span></p>

<h2>How does heat energy get inside Earth?</h2>

<p><img src="http://cdn4.explainthatstuff.com/geothermal.jpg" class="irightno" height="225" width="300" ALT="Hot geysers are a sign of geothermal energy"></p>

<p>It's hard to think of Earth as a hot place, especially if you live in somewhere like Alaska or
Greenland. But that's because we scrabble around on the surface of
the planet, exposed to changing seasons and cold winds. Underground,
it's a very different story. Volcanoes and geysers hint at how much
<a href="energy.html">energy</a> is being generated by nuclear
reactions firing off deep inside our planet, where even solid rocks turn to a hot bubbling soup.
Geologists have estimated that there's about 42 million megawatts of
this <span class="bold">geothermal energy</span> trapped inside Earth, which is equivalent to
the energy made by 25,000 large power plants!</p>

<P>Unfortunately, we can't easily access this kind of geothermal energy, except from a relatively few places
on Earth (think of Iceland or Yellowstone National Park). Even so, we can extract heat stored in
the ground pretty from much anywhere on the planet. How come? Instead of tapping into geothermal energy, most heat pumps make use of the simple fact that the ground is generally warmer than the atmosphere above it in winter because of the energy it systematically receives, year-round, from the Sun. (That's why it's often better to refer to them as <span class="bold">ground-source heat pumps</span> rather than geothermal heat pumps.) In the top 3m (10ft) or so of Earth's surface, the temperature stays a steady 10&ndash;20&deg;C (50&ndash;60&deg;F). So, even if you don't live on top of a volcano or have a geyser in your backyard,
not far below ground level, there's still a huge reservoir of heat just waiting to be tapped.</p>

<p><span class="credit">Photo: There's lots of geothermal energy
trapped inside Earth. Geysers like this occur when rainwater and melted snow drips down
through hot rocks then blasts back up again as boiling steam.
Picture by Robert Blackett, Utah Geological Survey, courtesy of 
<a target="_blank" href="http://images.nrel.gov/">US Department of Energy/National Renewable Energy Laboratory (DOE/NREL)</a>.</span></p>


<h2>How can you move heat?</h2>

<p><img src="http://cdn4.explainthatstuff.com/ground-source-heat-pumps-countries.png" class="ileftno" height="300" width="300" ALT="Pie chart showing that 56% of installed heat pump capacity is in the USA, 39 percent is in Europe, and 5% is in Asia."></p>

<p>Virtually all the heating systems
people use in buildings work by generating <a href="heat.html">heat energy</a> where it's needed.
<a href="electricity.html">Electric</a> radiators convert electrical
energy into heat by passing
electric currents through thin wires called filaments or elements
(similar to those in an <a href="electrictoasters.html">electric
toaster</a>).
Gas and oil central heating boilers burn energy-rich fuels with
oxygen from the air in a process called combustion. The energy
released in this way is used to heat hot <a href="water.html">water</A> 
that circulates through the radiators in our homes.</p>

<p>Heat <a href="pumpcompressor.html">pumps</A> work in a totally
different way. Instead of <span class="italic">creating</span> heat,
they simply pick it
up and <span class="italic">move</span> it from one place to
another&#8212;usually from the
ground under your home into the building itself. If that sounds
weird, just think for a moment about the refrigerator or air
conditioner unit in your home. A <a href="refrigerator.html">refrigerator</a>
also works by moving
heat from one place to another: it uses a cooling fluid to absorb
heat from the chiller compartment, move it outside, and then release
it into the room using radiator fins on the back of the case. An
<a href="airconditioner.html">air conditioner</a> works in a broadly similar way but cools down an entire
room (or an entire building). We can think of a heat pump as a bit
like a refrigerator or an air conditioner working in reverse: instead
of moving heat from the inside to the outside, it moves heat from the
outside to the inside&mdash;even in winter!</p>

<p><span class="credit">Chart: You might think heat pumps would be used only in really cold countries;
Sweden and Switzerland account for a quarter of the world's heat-pump capacity between them.
But the technology can be used pretty much anywhere. Half of worldwide installed heat pump capacity is in the United States (where there are over a million separate units), just over a third is in Europe (where Sweden dominates), and the remainder is in Asia. Statistics from Geothermal Heat Pumps: Overview of Market Status: February 2009 (Executive Summary), US Department Energy (see references below).</span></p>

<h2>How heat pumps work</h2>

<p><img src="http://cdn4.explainthatstuff.com/heatpump1.jpg" class="irightno" height="292" width="288" ALT="A heat pump inside a building"></p>

<p>In a central heating system,
there's a continuous circuit of <a href="water.html">water</a> pipes,
starting at the boiler,
flowing through all the radiators in turn, and going back to the
boiler again. When cold water flows into the boiler, it gets heated
up very rapidly. A pump circulates it through the radiator pipes,
where it gives off its heat and cools, before flowing back to the
boiler to pick up more heat and go round the loop again.</p>
<p>A heat pump system is very similar
but, instead of the boiler, the source of heat is the ground
underneath your home. Water (usually mixed with antifreeze) is forced
through a long string of plastic pipes buried in the ground outside
or directly underneath the building. The water picks up some of
Earth's heat and flows into the building, where the energy
is removed by a <a href="how-heat-exchangers-work.html">heat exchanger</a> and delivered through air ducts,
cooling the water in the process. The cooled water then passes back
outside to pick up some more heat from the ground.</p>


<p>
<span class="credit">Photo: A heat pump on the campus of Georgia Tech.
Photo courtesy of US Department of Energy.</span></P>

<p>You can see then that a heat pump
system really consists of three separate parts:</p>
<ol>
  <li><span class="bold">Ground loop</span>: The network of plastic pipes, filled with
water, that collects heat from Earth.</li>
  <li><span class="bold">Heat pump</span>: A simple water pump that moves the water around
a loop.</li>
  <li><span class="bold">Air duct</span>: Also known as the air handler, this extracts the
heat from the water and uses a
fan to blow it through your home.</li>
</ol>
<p>The only energy you have to supply
to the system is the electricity you need to power the pump and the
air blower unit. All the energy that actually <span class="italic">heats</span>
your home is coming from the ground.</p>

<h2>Types of ground-source heat pumps</h2>

<p><img src="http://cdn4.explainthatstuff.com/howheatpumpswork.png" height="300" width="500" ALT="Artwork comparing three different kinds of closed-loop heat pumps work: horizontal loop, vertical loop, and horizontal loop using a lake or pond" class="ileft"></P>

<p>Ground-loop pipes need to extract as much heat as possible and there are four different ways to achieve
this. Different methods work best for different types of buildings
depending on where they're located. For ordinary-sized homes, the
simplest and commonest installation involves running the ground-loop
pipes at a relatively shallow depth (about 1.5&ndash;2m or 4&ndash;6 ft) through
a relatively large horizontal area next to your house. Office
buildings and schools, which need to collect much more heat,
typically use a smaller horizontal area of pipes but run them
vertically much deeper (about 30&ndash;120m or 100&ndash;400ft). The heat doesn't
necessarily have to come from soil or rock. If you live near a large
pond or lake, you can use that as your heat source instead of the
ground. The pipes run horizontally about 2.5m (8ft) below the water
surface to prevent them from freezing up.</p>

<P>
<span class="credit">Artwork: Three types of closed-loop heat pump system:<BR>
1. A horizontal loop, where the pipes run near the ground surface but over a large area;
2. A vertical loop has much deeper pipes recovering more heat from deeper, warmer ground;
3. A horizontal loop using a lake or pond as its heat source.</span>
</P>

<p>These three types of system all use
<span class="bold">closed loops</span> of pipes (so the same fluid is contained within and
flows through the heating system at all times, like the coolant in a
refrigerator). There's also an alternative design, known as an
<span class="bold">open-loop</span> system, where you pump the water out from your house
into an open, underground well and then pump it back out again. The water
has to be very clean for you to be able to do this, however.</p>
<h2>Other advantages</h2>
<p>Heat pumps can warm your home in
winter, but they have another huge advantage too: run them in reverse
in summer and they can extract heat from your home and sink it back
into the ground&#8212;working just like air conditioners, only much more
cheaply. They can also supply you with free hot water in summer and
roughly half-price hot water in winter. Because the pipes are buried
underground, the ground-loops are virtually maintenance free and
(unlike air conditioners) practically impossible to damage or
vandalize. The manufacturers are so confident in their products that
heat pumps typically carry warranties of 25&ndash;50 years. Customers like
them too: according to the US Department of Energy, user satisfaction
ratings are about 90 percent!</p>

<h2>How good are heat pumps?</h2>
<p>In a word: superb! According to the
US Department of Energy, they give annual energy savings of 30&ndash;70
percent in heating costs in winter and 20&ndash;50 percent in cooling costs
in summer. That means they pay for themselves in 2&ndash;10 years. They're
about 50 percent more efficient than gas furnaces (gas central-heating boilers) and 75 percent more
efficient than oil furnaces (oil boilers). All round, they're good for your
pocket&#8212;and great for the planet. (Having said that, they
do need to be properly installed. One UK study published in 2010
found that 80 percent of heat pumps were badly installed and
massively underperforming&mdash;sometimes consuming more energy
than they actually produced.)</p>
