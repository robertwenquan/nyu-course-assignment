  <h2>Wine Philosophy</h2>
  
  <p>Uncle Jack's Steakhouse believes that wine is an integral part of the dining experience. We customize our wine lists to cater to the needs of guests at each location. If there is something you would like to see on our wine list, or would like to see a copy of our wine list, let us know by emailing <a href="mailto:winedirector@unclejacks.com">winedirector@unclejacks.com</a>.</p>
    
    <p>While we represent the best wines from across the globe, we are focused on small production, cult and ultra premium California Cabernet Sauvignon based wines. We have long standing relationships and are proud to offer many wines and vintages from the following producers: Abreau, Angelo Gaja, Bryant Family, Colgin, Chateau Montelena, Dominus, Domaine Romanee Conti, Felsina, Fontodi, Groth, Harlan, Jordan, Kistler, Lynch Bages, Mouton Rothschild, Molly Dooker, Opus One, Petrus, Paul Hobbs, Quilceda Creek, Ridge, Silver Oak, Sine Qua Non, Turley, as well as many other great producers.</p>
    <p>We have temperature and humidity controlled storage at all of our locations to insure proper aging and optimum quality. We purchase many wines for later release on our list as well as buy back vintages from around the world. Our staff goes through all levels of training, from the basics to all the details about specific wineries and regions. We offer the best wines from around the world at a great price so you can enjoy something special no matter what the occasion.</p>
      
