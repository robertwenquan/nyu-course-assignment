  <h1>Uncle Jack<span>'</span>s Philosophy</h1>
  
  <h2>Beef Philosophy</h2>
    
    <img src="files/images/content/best-restaurants-in-new-york-city.jpg" alt="Best Restaurants in New York City Uncle Jack's" width="300" height="200" class="imageRight"/>
      
      <p><strong>USDA Prime</strong><br />
Our beef is hand chosen by owner William Degel, who works directly with ranches in Nebraska &amp; Omaha who raise the cattle to our exact specifications. Once received beef is perfectly aged on premise for 28-35 days, depending on size, for optimum flavor &amp; moisture. We have aging boxes to control time, temperature &amp; humidity in all of our locations so every piece of beef is perfect before it is cooked.</p>

<p>We cook all of our steaks in 1800-degree infrared broilers that provide a great char while searing in <br />
all the juices in the meat; steaks are then finished to desired temperatures and served with our <br />
natural au jus.</p>

<p>The United States Department of Agriculture Highest Beef Grade is Prime, which is the only grade we use at Uncle Jack's Steakhouse. Currently 2% of all US beef is graded at Prime. The grading comes from quality and content of intra-muscular fat.</p>

<p><strong>Kobe Beef</strong><br />

Kobe beef refers to beef from the black Tajima-ushi breed of Wagyu cattle, raised according to strict tradition in Kobe, Japan. Kobe beef is renowned for its flavor, tenderness, and fatty well-marbled texture and flavor..</p>

<p><strong>Kobe Style</strong><br />
The massive increase in popularity of Kobe beef in the United Sates has led to the creation of "Kobe-style" beef, taken from domestically-raised Wagyu crossbred with Angus cattle, in order to meet the demand. Farms in America and Britain have attempted to replicate the Kobe traditions, providing their Wagyu herds beer and daily massages. U.S. meat producers claim that any differences between their less expensive "Kobe-style" beef and true Kobe beef are largely cosmetic. This <br />
has been brought into question, however, because the cattle are fed American and / or British grass and grain, which is different from the more expensive <br />
Japanese feed.</p>
<p> Uncle Jack's offers only Authentic Japanese Kobe Beef.</p>
