<P>If you like what you've read on Explain that Stuff, you're going to love my new book,
<B>Atoms under the Floorboards: The Surprising Science Hidden in Your Home</B>, which is published worldwide by Bloomsbury
(the guys who gave us Harry Potter).</P>

<P>From the bookshelf that's always dusty to the shiny shoes on your feet,
I take you on a whistle-stop tour of your home, revealing fascinating
science secrets like...</P>

<UL>
<LI>How many "pedalling" hamsters would you need to boil the water for a coffee?</LI>
<LI>How did a "worm" play a part in the history of radio?</LI>
<LI>Why can't you cook your dinner with a cellphone?</LI>
<LI>Why can you see through windows but not through walls?</LI>
<LI>Why does your laptop get hot when its only moving part is the cooling fan?</LI>
<LI>What's the connection between a wedding dress, a suspension bridge, and a bicycle?</LI>
</UL>

<P>Just like Explain that Stuff, it's an easy read for non-scientists&mdash;adults and teenagers&mdash;and there's
little or no math.</P>

<h2>So what's inside this book?</h2>

<P><I>Atoms Under the Floorboards</I> is about 300 pages long, has 60 black-and-white artworks to liven things up a bit,
and there's a short section of notes and further reading at the end to help you dig deeper into the bits that interest you.</P>

<P><img src="http://cdn4.explainthatstuff.com/atoms-under-floorboards-large.jpg" class="irightno" alt="Atoms under the floorboards book cover." height="380" width="247"></P>

<P>There are 18 chapters&mdash;and here's a taster of the kinds of things they cover:</P>
<OL>
<LI><B>Firm foundations</B>: Why don't buildings fall down? Why do skyscrapers deliberately wobble in the wind?</LI>
<LI><B>Upstairs downstairs</B>: Everything you ever wanted to know about energy, including: How many hamsters running on wheels would you need to launch a rocket or haul a train?</LI>
<LI><B>Superheroics</B>: You might not think you're a superhero, but science can turn you into one! How do wheels really work... and why is Isaac Newton the best swimming teacher?</LI>
<LI><B>The beauty of bikes</B>: Why are cyclists like fish? Can shaving your legs really make you go faster?</LI>
<LI><B>Car crazy</B>: Why is your car like a chemistry lab on wheels? Why have electric cars still not caught on?</LI>
<LI><B>Sticky stuff</B>: How do glues really work? Why can you lift a person with one drop of superglue?</LI>
<LI><B>The inside story</B>: How do we know atoms exist if we can't see them?</LI>
<LI><B>Amazing glazing</B>: What's the science behind windows that clean themselves... and sunglasses that darken automatically outdoors?</LI>
<LI><B>Saggy sofas, squeaky floors</B>: Why do sofas sag and faces wrinkle? Why do floorboards squeak?</LI>
<LI><B>Light delights</B>: Why can't we see light? Why do polished shoes shine like mirrors?</LI>
<LI><B>Radio gaga</B>: Who was the "worm" who helped to invent radio? How did the Victorians almost invent mobile phones?</LI>
<LI><B>Living by numbers</B>: How can you send a photo down a phone line? Is it better to buy MP3s or CDs?</LI>
<LI><B>Blowing hot and cold</B>: How can you heat your home with nothing more than a candle? How can you make instant ice cream?</LI>
<LI><B>Food miles</B>: Is it true that you could "go to work on an egg"? Could we ever make an energy pill that gives us all the daily nutrition we need?</LI>
<LI><B>Stirring stuff</B>: What links wind turbines, dusty bookshelves, dirty cars, and rubbery custard?</LI>
<LI><B>Water, water</B>: What makes a drain gurgle&mdash;and how does that help us understand how pens work?</LI>
<LI><B>Stain games</B>: How can science get your jeans clean? Why can you dry clothes outdoors even in winter?</LI>
<LI><B>Dressing to impress</B>: Why don't sheep shiver in the cold? Why do jeans always wear out at the knees?</LI>
</OL>

